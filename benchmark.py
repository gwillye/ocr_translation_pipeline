# -*- coding: utf-8 -*-
"""
benchmark.py — Tesseract vs TrOCR, and the effect of OpenCV preprocessing.

Runs every engine/preprocessing combination over the ground-truthed sample from
``sample_data.py``, computes CER and WER with ``metrics.py`` (jiwer when
available), and writes a Markdown results table to ``RESULTS.md``. All numbers
are REAL — produced by running the engines here, never hand-written.

Engine / preprocessing pipelines compared
------------------------------------------
  * Tesseract (raw)            — global threshold off, image as-is.
  * Tesseract (PIL preprocess) — original ocr_utils pipeline (global threshold).
  * Tesseract (OpenCV adaptive)— adaptive threshold + deskew (preprocessing.py).
  * TrOCR (raw)                — microsoft/trocr-base-printed on the raw image.

TrOCR is a single-line model and expects the original photo/scan (it has its own
learned normalisation), so we feed it the raw image; the OpenCV experiment is
about whether *Tesseract* benefits from adaptive thresholding + deskew on the
degraded/skewed conditions.

Usage:
    python benchmark.py                 # full run (downloads TrOCR ~1.4GB once)
    python benchmark.py --no-trocr      # Tesseract + OpenCV only (no torch)
    python benchmark.py --out RESULTS.md
"""
from __future__ import annotations

import argparse
import statistics
import time
from typing import Callable, Dict, List, Optional

import numpy as np

import metrics
import ocr_utils
import preprocessing
from sample_data import Sample, build_dataset

# Each engine pipeline maps a PIL image -> recognised string.
Pipeline = Callable[["object"], str]


def _tess_raw(img) -> str:
    return ocr_utils.ocr_image(img, lang="eng", preprocess=False)


def _tess_pil(img) -> str:
    return ocr_utils.ocr_image(img, lang="eng", preprocess=True)


def _tess_opencv(img) -> str:
    prepped = preprocessing.adaptive_preprocess_pil(img, do_deskew=True)
    return ocr_utils.ocr_image(prepped, lang="eng", preprocess=False)


def _make_trocr_pipeline() -> Pipeline:
    from trocr_engine import get_engine

    engine = get_engine()

    def _run(img) -> str:
        return engine.read(img)

    return _run


def evaluate(
    pipeline: Pipeline,
    samples: List[Sample],
    conditions: Optional[List[str]] = None,
) -> Dict[str, float]:
    """Average CER/WER (and latency) of ``pipeline`` over the chosen samples."""
    cers, wers, times = [], [], []
    for s in samples:
        if conditions and s.condition not in conditions:
            continue
        t0 = time.perf_counter()
        pred = pipeline(s.image)
        times.append(time.perf_counter() - t0)
        # Case-insensitive: trocr-base-printed emits ALL-CAPS, so a
        # case-sensitive metric would unfairly penalise it for a cosmetic diff.
        cers.append(metrics.cer(s.text, pred, case_sensitive=False))
        wers.append(metrics.wer(s.text, pred, case_sensitive=False))
    return {
        "cer": statistics.mean(cers) if cers else float("nan"),
        "wer": statistics.mean(wers) if wers else float("nan"),
        "latency_s": statistics.mean(times) if times else float("nan"),
        "n": len(cers),
    }


def _fmt_row(name: str, r: Dict[str, float]) -> str:
    return (
        f"| {name} | {r['n']} | {r['cer']:.3f} | {r['wer']:.3f} "
        f"| {r['latency_s']*1000:.0f} |"
    )


def run(out_path: str = "RESULTS.md", use_trocr: bool = True, seed: int = 0) -> str:
    samples = build_dataset(seed)
    conds = ["clean", "degraded", "skewed"]

    engines: Dict[str, Pipeline] = {
        "Tesseract (raw)": _tess_raw,
        "Tesseract (PIL global threshold)": _tess_pil,
        "Tesseract (OpenCV adaptive + deskew)": _tess_opencv,
    }
    trocr_error = None
    if use_trocr:
        try:
            engines["TrOCR (base-printed)"] = _make_trocr_pipeline()
        except Exception as exc:  # torch/transformers/network unavailable
            trocr_error = str(exc)

    # ---- overall table (all conditions) ----
    lines: List[str] = []
    lines.append("# OCR Benchmark Results\n")
    lines.append(
        f"_Generated by `benchmark.py` over {len(samples)} ground-truthed "
        f"single-line images "
        f"({len(samples)//len(conds)} sentences x {len(conds)} conditions). "
        f"Metric backend: **{metrics.backend()}**. CER/WER are "
        f"case-insensitive (see note below). Lower is better._\n"
    )

    lines.append("\n## Overall (all conditions)\n")
    lines.append("| Engine / preprocessing | N | CER | WER | latency (ms/img) |")
    lines.append("|---|--:|--:|--:|--:|")
    overall: Dict[str, Dict[str, float]] = {}
    for name, pipe in engines.items():
        r = evaluate(pipe, samples)
        overall[name] = r
        lines.append(_fmt_row(name, r))

    # ---- per-condition (focus on OpenCV effect) ----
    lines.append("\n## Per-condition CER (the OpenCV effect)\n")
    header = "| Engine / preprocessing | " + " | ".join(conds) + " |"
    lines.append(header)
    lines.append("|---|" + "--:|" * len(conds))
    for name, pipe in engines.items():
        cells = []
        for c in conds:
            r = evaluate(pipe, samples, conditions=[c])
            cells.append(f"{r['cer']:.3f}")
        lines.append(f"| {name} | " + " | ".join(cells) + " |")

    if trocr_error:
        lines.append(
            "\n> **Note:** TrOCR was requested but could not run in this "
            f"environment:\n> `{trocr_error}`\n>\n"
            "> The code path is implemented and runs where torch+transformers "
            "are available; reproduce with `python benchmark.py` after "
            "`pip install torch transformers`.\n"
        )

    lines.append(
        "\n> CER/WER are computed case-insensitively: "
        "`microsoft/trocr-base-printed` emits ALL-CAPS text, so a "
        "case-sensitive metric would penalise it for a purely cosmetic "
        "difference. Tesseract preserves case either way.\n"
    )

    report = "\n".join(lines) + "\n"
    with open(out_path, "w", encoding="utf-8") as fh:
        fh.write(report)
    return report


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--out", default="RESULTS.md")
    ap.add_argument("--no-trocr", action="store_true", help="skip the TrOCR engine")
    ap.add_argument("--seed", type=int, default=0)
    args = ap.parse_args()
    report = run(out_path=args.out, use_trocr=not args.no_trocr, seed=args.seed)
    print(report)
    print(f"\n-> wrote {args.out}")


if __name__ == "__main__":
    main()
